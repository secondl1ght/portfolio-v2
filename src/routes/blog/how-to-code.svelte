    <h2>9. Choose a Specific Field that Interests You</h2>

    <p>
      For me this was <strong>bitcoin</strong>, I love bitcoin and wanted to
      specifically get a developer job in the bitcoin industry. By choosing a
      more niche area to focus your applications, you potentially narrow down
      the amount of interest from other applicants. You also get to showcase
      your additional <strong>domain specific knowledge</strong> on the subject market
      you are applying for, which gives you an edge.
    </p>

    <p>
      The development world is vast so you should be able to <strong
        >zone-in</strong
      > on a specific area that interests you. Other people will recommend to just
      apply to as many jobs as possible, but I think if you focus on an area that
      you are truly passionate about, then you will be even more likely to get a
      position.
    </p>

    <h2>10. Don't get Discouraged</h2>

    <p>
      The job search may take several months and during that time you need to
      stay motivated and <strong>continue to code</strong>. Coding is a
      <strong>life-long journey</strong> and taking a significant amount of time
      off will likely affect your progress.
    </p>

    <p>
      Don't get burnt out, keep your eye on the goal and do not get discouraged
      when you will inevitability get turned down. If you have put the time and
      effort in, and know how to code, then it is only <strong
        >a matter of time</strong
      > before someone recognizes this and takes you onto their team.
    </p>

    <p>
      There might be people who do not believe in you or recognize your skillset
      and drive, do not listen to them. Always remain <strong
        >focused and determined</strong
      > to achieve your goal of becoming a developer.
    </p>

    <h2>11. Once You Get The Job</h2>

    <p>
      <strong>Congratulations on becoming a Developer!!!</strong> Now bust your
      ass and show the company that hired you that they made the right decision.
      This is where the real learning starts. After you get real job experience
      you are off to the races, many
      <strong>doors will open up for you</strong>. Skilled developers are in
      high demand and it will remain this way. Don't just be a developer,
      <strong>be a good one</strong>.
    </p>

    <p>
      Being a developer means being a problem solver, use all the resources
      available to you to solve these problems.
    </p>

    <p>
      Have fun, stay humble, and enjoy your new found career, you put the work
      in and <strong>you earned it!</strong>
    </p>

    <h2>12. Extra Tips and Tricks</h2>

    <p>Here is some extra info that I found useful:</p>

    <h3>Stay Organized</h3>

    <p>
      There will be a flood of information that you will learn at the beginning,
      it's important to <strong>stay organized</strong> to keep track of everything.
    </p>

    <p>
      For me this meant creating an extensive bookmarks bar in my web browser.
      Yes, a bookmarks bar with 100's of links. I have a category for each piece
      of tech that I use and sub-categories within these. For example, I have an
      HTML, CSS, JavaScript, React, Svelte, and so on bookmark folder where I
      keep valuable resources that I can refer back to as I work.
    </p>

    <h3>Get Off the Platform ASAP</h3>

    <p>
      In your course you will likely have an embedded text editor to get you
      started. As soon as you can, start working on setting up your <strong
        >local dev environment</strong
      >. This means being familiar with the command line and installing a
      <strong>text editor</strong>
      (I recommend
      <a target="_blank" rel="noreferrer" href="https://atom.io/">Atom</a>).
    </p>

    <p>
      It's important to start getting comfortable and familiar with your local
      dev environment, <strong>this is your battle-station</strong>.
    </p>

    <h3>Other Guides</h3>

    <p>
      There are lots of guides out there for creating a portfolio, resume,
      interview-prep etc. Check out lots of other guides.
    </p>

    <h3>Get Some Real Hardware</h3>

    <p>
      Once you have given coding a try and decided that this is the path for you
      - it would be good to invest in <strong>proper hardware</strong>. A nice
      laptop with a good amount of RAM, a good wireless mouse and mechanical
      keyboard. For operating systems I highly recommend using
      <a
        target="_blank"
        rel="noreferrer"
        href="https://www.linux.org/pages/download/">Linux</a
      >. Do some research on different Linux distributions, but as a beginner I
      would recommend PopOS.
    </p>

    <p>
      Also invest in a good chair and <strong
        >take pride in your coding setup</strong
      > - you will be spending many hours here, so make it awesome!
    </p>

    <h3>Listen to Coding Podcasts / YouTube</h3>

