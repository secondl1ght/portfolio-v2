    <p>
      I will structure this post into the main steps that I took along my path
      and at the end I will include some general tips and tricks that I have
      learned along the way.
    </p>

    <p>
      The first thing I think it is important to understand as you embark on
      this quest is that what you are doing is <u>not crazy</u>. It is entirely
      possible to teach yourself how to code and in many ways it is actually
      <strong>preferable</strong>
      than following a traditional path, such as university with a computer science
      degree. You get to learn faster, save money, and start your career much sooner
      by going this route. The only requirements are
      <strong>time</strong>, <strong>effort</strong>, and a
      <strong>commitment to achieving your goals</strong>. You can realistically
      teach yourself the fundamentals and get a position as a Junior Developer
      in about 3-6 months. If you commit to coding at least a few hours a day
      for 5 days a week. I have heard many stories of people who were able to
      achieve this even while managing many other difficult life tasks, like
      raising kids. If you want to make this happen, you <u>CAN</u> do it.
    </p>

    <p>
      With that being said, here is what I found worked for me. There are many
      ways of doing this such as bootcamps, but the truly self-taught route is
      by far the cheapest and best option for people who are <strong
        >self-driven</strong
      >
      and <strong>motivated to learn</strong>, in my humble opinion.
    </p>

    <p>Now, onto the guide:</p>

    <h2>1. Choose an Area to Focus</h2>

    <p>
      The first step is to decide an area to focus. Development is a massive
      subject and there are many sub-categories to choose from. I would
      personally recommend choosing between either <strong>Back-End</strong> or
      <strong>Front-End</strong>, as these 2 positions tend to be the most
      popular for finding a job once you are ready.
    </p>

    <p>
      But there are many other positions such as <strong>Dev-Ops</strong>,
      <strong>Data Analyst</strong>, <strong>Quality Assurance</strong> and more.
      Do a bit of research on each category of development and see what peaks your
      interest. For me, the visual aspect of front-end development is what I found
      to be a great starting point into the big world of development.
    </p>

    <p>
      Within these main categories there are further areas you can focus, for
      example <strong>web development</strong> vs
      <strong>mobile development</strong>. My recommendation is web, that is
      what I have found the most interesting personally, but everyone is
      different. Desktop applications are still developed but for the broadest
      job possibilities, I believe web or mobile are the best choices.
    </p>

    <h2>2. Pick a Learning Platform and Start Teaching Yourself!</h2>

