    <h2>5. Start Working on Projects</h2>

    <p>
      This may be required as part of the course you are taking, you should make <strong
        >at least 3 different projects</strong
      >
      with whatever technologies you are learning. Learning from your course is good,
      but the real learning happens when you go off into the wild and
      <strong>build something from scratch</strong>.
    </p>

    <p>
      It doesn't have to be super complicated, but challenge yourself to put the
      skills you have been learning to use, and then showcase these projects in
      your portfolio. I will link a couple of my personal projects that I worked
      on while I was learning to code here. You will see that they are fairly
      simple, but I learned <u>SO</u> much by building these and actually writing
      code to solve problems in real-life.
    </p>

    <p>
      This is a <u>MUST</u> to compliment your course work. When building projects,
      choose ideas that are fun to you. If you are enjoying the project, it will
      make it much easier to finish. Don't just build a generic X project, come up
      with a cool idea and run with it.
    </p>

