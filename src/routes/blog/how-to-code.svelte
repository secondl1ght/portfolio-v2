    <h3>Get Really Good at the Internet</h3>

    <p>
      Understand that a big part of coding is searching the internet for
      solutions to problems. It is normal for senior developers to search up
      questions to problems they are facing. <strong
        >The internet is awesome</strong
      > - use it.
    </p>

