    <h2>6. Utilize Community Resources to get Un-stuck</h2>

    <p>
      There will be points in your self-taught journey that you get stuck. Not
      having a teacher or fellow students to ask questions can be hard. Luckily
      there are many online communities that you can turn to for asking
      questions.
    </p>

    <p>
      Whatever tech you are using likely has a <strong>Discord channel</strong>.
      If you are learning React for example, make sure you join the React
      discord and ask questions there. You may even find a
      <strong>mentor</strong> willing to help you on an ongoing basis.
    </p>

    <p>
      When asking questions make sure you have <strong
        >put the work in yourself first</strong
      >, don't use these communities as a crutch. When you first encounter an
      error message your first response should not be to post it in the chat.
      Make sure to spend at least an hour or more trying to figure it out on
      your own first. This is will help you learn,
      <strong>debugging is half of programming really</strong>.
    </p>

    <h2>7. Contribute to Open Source</h2>

    <p>
      After you have built a few of your own projects I would recommend <strong
        >contributing to open source</strong
      >. Find an open source project that you are interested in and start
      getting familiar with working with others through contributions.
    </p>

    <p>
      The open source community is very welcoming in general and you should be
      able to find somewhere you can help. There is an annual open source
      hackathon for beginners called <a
        target="_blank"
        rel="noreferrer"
        href="https://hacktoberfest.digitalocean.com/">Hacktoberfest</a
      >
      which is a <u>GREAT</u> way to start out.
    </p>

    <p>
      Consider your open source contributions as your first real work experience
      and make sure all of your work is <strong
        >professional and high quality</strong
      >.
    </p>

    <h2>8. Create a Resume and Job Prep</h2>

    <p>
      Once you have completed your course, created a GitHub profile and a
      portfolio with a few projects, joined some online communities, and made
      open source contributions, I think you are ready to make a resume and
      start preparing for applications!
    </p>

    <p>
      By this point you should have a <strong
        >solid understanding in the fundamentals</strong
      >
      of whatever dev category you have chosen. You should feel comfortable working
      with your tech stack and <strong>speaking technically</strong> to other developers
      at an entry level.
    </p>

    <p>
      There are many guides to interviews and resumes so I won't go into too
      much detail here, but try to make your <strong
        >enthusiasm stand out</strong
      > from the rest.
    </p>

    <p>
      When prepping for interviews you may be intimidated by the level of
      knowledge some people are telling you to study. Certain companies
      purposefully use extremely hard coding challenges for their interviews,
      but I think the industry as a whole is moving away from this method.
      Unless you are trying to get a job in Big Tech <em
        >(which I wouldn't recommend)</em
      >, then I think having more
      <strong>applicable, job-based knowledge</strong> should be enough to get you
      in the door.
    </p>

    <p>
      Being a self-taught developer you will need <strong
        >someone to take a chance on you</strong
      >. But once you get your foot in the door, you will be able to
      <strong>work hard, and prove yourself to be a valuable team member</strong
      >.
    </p>

    <p>
      Most jobs are now remote so you should be able to apply to a broad range
      of positions <strong>all around the world</strong>. Don't be afraid to
      take contract/freelance positions with small startups companies. This is
      normal and while it may not be as secure as a traditional job, it is a
      good way to get experience.
    </p>

