    <p>
      As you go through your chosen course remember to pace yourself, <strong
        >development is a marathon not a sprint</strong
      >. There may be times where you get overwhelmed and stuck. If you can't
      solve a specific problem, <strong>don't get discouraged</strong>. If you
      persevere, you <u>will</u> be able to look back at the same problem months
      later and understand it with ease.
    </p>

    <p>
      If you are truly stuck then don't be afraid to move on and come back
      later. If you think something is not actually relevant to your future
      career goals then you can even skip some sections entirely. For example,
      some courses may teach you Data Structures and Algorithms. While these may
      be good to know, they are likely not necessary for your first job. You
      will be learning at a very fast pace and processing lots of new
      information, so it is important to focus on the entry-level skills that
      you will need to get a job and not get lost into some more advanced
      concepts at the beginning.
    </p>

    <h2>3. Setup a GitHub Account</h2>

    <p>
      <a target="_blank" rel="noreferrer" href="https://github.com/">GitHub</a>
      gives you the ability to showcase all of your work to the world and future
      employers. <strong>Make all of your work open source</strong>. Many
      employers will review your GitHub to see your activity and your code. You
      will learn how to use Git as you take your course, but getting this
      profile setup as early as possible is a good idea in my opinion.
    </p>

    <h2>4. Create a Portfolio</h2>

    <p>
      Once you have some fundamentals in your tech stack under your belt you
      should start working on your portfolio. <strong
        >Make it interesting and make it look good</strong
      >. Don't be afraid to use tech like Bootstrap components, or borrow ideas
      from templates. This will be the first real project you work on, so if you
      don't code it from the ground up that is OK. The goal is to make a nice
      looking site that showcases your skills and tells people about yourself.
      If you are focusing on back-end development, then you could even use a
      template for this. Employers will not necessarily be too concerned about
      your front-end abilities.
    </p>

    <p>
      <strong>Have fun with this</strong>, showcase yourself to the world and
      let everyone know what interests you have! As you create projects you can
      update this website. It will be a living site as you progress and gain new
      skills to show-off. I will link my site here for some ideas - design is
      not my strong-suit, so there are much better looking portfolios out there.
      However this will give you an idea on the details to include in your own
      portfolio.
    </p>

